function mostrarFrase(tipo) {
    confetti({ particleCount: 150, spread: 70, origin: { y: 0.6 }, colors: ['#6a1b9a', '#0288d1', '#c6ff00'] });

    const cajaFrases = document.getElementById('frase');
    const lista = frases[tipo];
    const aleatoria = lista[Math.floor(Math.random() * lista.length)];

    cajaFrases.classList.add('animar-cambio');
    setTimeout(() => {
        cajaFrases.textContent = aleatoria;
        cajaFrases.classList.remove('animar-cambio');
    }, 300);
}

function reiniciarMensaje() {
    const cajaFrases = document.getElementById('frase');
    cajaFrases.style.opacity = 0;
    setTimeout(() => {
        cajaFrases.textContent = "¿CÓMO TE SIENTES HOY MISS YEIMY?";
        cajaFrases.style.opacity = 1;
    }, 300);
}

