function saludarSegunHora() {
    const saludoElemento = document.getElementById('saludo');
    const hora = new Date().getHours();
    let mensaje = "¡Hola, Maestra Yeimy!"; // Por defecto

    if (hora >= 5 && hora < 12) {
        mensaje = "¡Buenos días, Miss Yeimy! ☀️";
    } else if (hora >= 12 && hora < 19) {
        mensaje = "¡Buenas tardes, Miss Yeimy! 🌷";
    } else {
        mensaje = "¡Buenas noches, Miss Yeimy! 🌙";
    }

    saludoElemento.textContent = mensaje;
}

